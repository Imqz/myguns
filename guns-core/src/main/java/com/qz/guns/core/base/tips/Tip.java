package com.qz.guns.core.base.tips;

/**
 * @author qinzhi
 * @date 2018/10/11 11:13
 */
public class Tip {
    protected int code;
    protected String message;

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }
}
